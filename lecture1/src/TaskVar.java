
public class TaskVar {

	/**
	 *125 + 24 
	 *125 - 24 
	 *125 x 24 
	 *125 / 24 
	 *125 % 24
	 */
	public static void main(String[] args) {
		int numAdd;
		numAdd = 125 / 24;
		
		System.out.println(numAdd + " = 125 / 24");
		
		int numAdd1 = 125 + 24;
		
		System.out.println(numAdd1 + " = 125 + 24");
		
		
		int numAdd2;
		int num1 = 125;
		int num2 = 24;
		numAdd2 = num1 + num2;
		
		System.out.println(numAdd2 + " = " + num1 + " + " + num2);
		
	}

}
