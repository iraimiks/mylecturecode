
public class PrintNumberInWord {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		//Using equal operator == to check if value is 
		int number = 3;
		
		if(number == 1){
			System.out.println("ONE");
		}else if(number == 2){
			System.out.println("TWO");
		}else{
			System.out.println("No number in row");
		}
		

	}

}
